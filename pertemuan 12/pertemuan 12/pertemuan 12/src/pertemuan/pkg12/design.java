    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        try {
       // Tambahan: Cek duplikat
        java.sql.Connection conn = koneksi.configDB();
        java.sql.PreparedStatement cek = conn.prepareStatement("SELECT * FROM catatwisata WHERE kode_catatan = ?");
        cek.setString(1, formnim.getText());
        java.sql.ResultSet hasil = cek.executeQuery();
        if (hasil.next()) {
            JOptionPane.showMessageDialog(this, "Kode Catatan sudah digunakan! Klik Tambah lagi untuk buat kode baru.");
            // Buat kode baru
            return;
        }

        // Kode lama kamu tetap seperti ini (tidak diubah):
        String sql = "INSERT INTO catatwisata VALUES('" + formnim.getText() + "','" + formnama.getText() + "','" + formalmt.getText() + "','" + ulasan_.getText() + "')";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.execute();
        JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        load_tabel();
         // Tambahan: langsung generate kode baru setelah tambah
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }

    }//GEN-LAST:event_btntambahActionPerformed

