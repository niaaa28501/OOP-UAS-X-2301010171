private void load_tabel() {
 {
 DefaultTableModel model = new
 DefaultTableModel();
 model.addColumn("No");
 model.addColumn("Kode Catatan");
 model.addColumn("Tempat Wisata");
 model.addColumn("Tanggal");
 model.addColumn("Ulasan");

 //menampilkan data database kedalam tabel
 try {
 int no=1;
    String sql = "select * from catatwisata"; java.sql.Connection
conn=koneksi.configDB();
 java.sql.Statement
stm=conn.createStatement();
 java.sql.ResultSet
res=stm.executeQuery(sql);
 while(res.next()){
 model.addRow(new
Object[]{no++,res.getString(1),res.getString(2), res.getString(3), res.getString(4)});
 }
 tabelinput.setModel(model);

 } catch (SQLException e) {
 }
 }
 }
